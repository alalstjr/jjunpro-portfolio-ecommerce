CREATE TABLE ACCOUNT
(
    ID        SERIAL PRIMARY KEY,
    USERNAME  VARCHAR,
    EMAIL     VARCHAR NOT NULL UNIQUE,
    FIRSTNAME VARCHAR,
    LASTNAME  VARCHAR,
    PASSWORD  VARCHAR,
    ENABLED   BOOLEAN NOT NULL,
    USERROLE  VARCHAR NOT NULL,
    AGERANGE  VARCHAR,
    BIRTHDAY  VARCHAR,
    GENDER    VARCHAR
);

